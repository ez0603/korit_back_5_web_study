// 산술 연산자
console.log(10 + 20);
console.log(40 - 20);
console.log(10 * 2);
console.log(10 / 2);
console.log(10 % 3);

// 비교 연산자
console.log(10 == "10"); // true -> 타입비교 X
console.log(10 === "10"); // false -> 타입비교 O (자료형까지 일치해야 true)
console.log(10 != "10"); // false -> 타입 비교를 안하고 둘이 같으니 !(부정)에서 false
console.log(10 !== "10"); // true 

console.log(10 + "10"); // 1010 출력